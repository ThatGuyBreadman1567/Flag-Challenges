import turtle
turtle.left(90)
turtle.pencolor('green')
turtle.fillcolor('green')
turtle.begin_fill()
turtle.forward(300)
turtle.left(90)
turtle.forward(500/3)
turtle.left(90)
turtle.forward(300)
turtle.left(90)
turtle.forward(500/3)
turtle.end_fill()
turtle.pencolor('black')
turtle.forward(500/3)
turtle.left(90)
turtle.forward(300)
turtle.left(90)
turtle.forward(500/3)
turtle.left(90)
turtle.penup()
turtle.forward(300)
turtle.pendown()
turtle.left(90)
turtle.forward(500/3)
turtle.pencolor('red')
turtle.fillcolor('red')
turtle.begin_fill()
turtle.forward(500/3)
turtle.left(90)
turtle.forward(300)
turtle.left(90)
turtle.forward(500/3)
turtle.left(90)
turtle.forward(300)
turtle.end_fill()
turtle.done()